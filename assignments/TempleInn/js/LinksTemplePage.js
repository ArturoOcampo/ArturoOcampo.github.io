function LinkMadridTemple() {
    var Ma = document.getElementById("Madrid");
    var Me = document.getElementById("Mexico");
    var Oa = document.getElementById("Oakland");
    var Sa = document.getElementById("Sacramento");

    Ma.style.display = "none";
    Me.style.display = "none";
    Oa.style.display = "none";
    Sa.style.display = "none";

    if (Ma.style.display === "none") {
        Ma.style.display = "block";
    } else {
        Ma.style.display = "block";
    }
    location.href = "#Madrid";
}

